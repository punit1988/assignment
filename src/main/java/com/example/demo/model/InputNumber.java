package com.example.demo.model;

public class InputNumber {

	int x;
	int y;
	
	public InputNumber() {
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	@Override
	public String toString() {
		return "InputNumber [x=" + x + ", y=" + y + "]";
	}
	
  
}
